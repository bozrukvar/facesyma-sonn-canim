/**
 * Get translated string for key and language
 * Falls back to English, then returns key if not found
 */
export const t = (key: string, lang: string): string => {
  const translation = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
  return translation;
};

/**
 * Get all translations for a language
 */
export const getLangTranslations = (lang: string): Record<string, string> => {
  return TRANSLATIONS[lang] ?? TRANSLATIONS['en'];
};
