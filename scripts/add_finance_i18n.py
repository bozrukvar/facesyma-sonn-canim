ANCHOR = "'xp.already_claimed'"


def run():
    content = I18N_FILE.read_text(encoding="utf-8")
    lines = content.split("\n")
    updated = 0

    for lang, keys in FINANCE_KEYS.items():
        # Find the lang section header
        lang_idx = None
        for i, line in enumerate(lines):
            if line.strip().startswith(f"{lang}:") and "{" in line:
                lang_idx = i
                break

        if lang_idx is None:
            print(f"  ✗ '{lang}' dil bloku bulunamadi")
            continue

        # Find anchor key AFTER the lang section
        anchor_idx = None
        for i in range(lang_idx, min(lang_idx + 3000, len(lines))):
            if ANCHOR in lines[i]:
                anchor_idx = i
                break

        if anchor_idx is None:
            print(f"  ✗ '{lang}' icin anchor bulunamadi")
            continue

        # Check which keys already exist in this language block
        block_text = "\n".join(lines[lang_idx:min(anchor_idx + 200, len(lines))])
        new_lines_to_insert = []
        for key, val in keys.items():
            if f"'{key}'" in block_text:
                continue
            safe_val = val.replace("'", "\\'")
            new_lines_to_insert.append(f"    '{key}': '{safe_val}',")

        if not new_lines_to_insert:
            print(f"  - '{lang}': zaten guncel")
            continue

        insert_pos = anchor_idx + 1
        lines = lines[:insert_pos] + new_lines_to_insert + lines[insert_pos:]
        updated += len(new_lines_to_insert)
        print(f"  ✓ '{lang}': {len(new_lines_to_insert)} anahtar eklendi")

    I18N_FILE.write_text("\n".join(lines), encoding="utf-8")
    print(f"\nTamamlandi: {updated} anahtar eklendi.")


if __name__ == "__main__":
    run()
