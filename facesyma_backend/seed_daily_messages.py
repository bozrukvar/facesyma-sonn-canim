def seed():
    for db_name, collections in MESSAGES.items():
        db = client[db_name]
        for coll_name, messages in collections.items():
            coll = db[coll_name]
            coll.drop()
            field = f"{coll_name}_daily"
            coll.insert_one({field: messages})
            print(f"  {db_name}.{coll_name}: {len(messages)} messages seeded")
    print("Done.")

if __name__ == '__main__':
    seed()
