def populate_language(lang_code):
    """Populate all strings for a language"""
    if lang_code not in ALL_TRANSLATIONS:
        return 0

    locale_dir = BASE_DIR / 'locale'
    po_file = locale_dir / lang_code / 'LC_MESSAGES' / 'django.po'

    if not po_file.exists():
        return 0

    translations = ALL_TRANSLATIONS[lang_code]

    with open(po_file, 'r', encoding='utf-8') as f:
        content = f.read()

    translated_count = 0

    for english, translated in translations.items():
        pattern = f'msgid "{english}"\nmsgstr ""'
        replacement = f'msgid "{english}"\nmsgstr "{translated}"'

        if pattern in content:
            content = content.replace(pattern, replacement)
            translated_count += 1

    with open(po_file, 'w', encoding='utf-8') as f:
        f.write(content)

    return translated_count


